Here you can register the languages that shall be used by your site. You can select from a list of existing languages, or create a definition from scratch.

Every language has a number of fields that must be specified:

<ul>
	<li><strong>System Name:</strong> the name of the language in your native tongue for easy recognition.</li>
	<li><strong>Native Name:</strong> the name of the language as it should appear to a native speaker.</li>
	<li><strong>Short Name:</strong> a shorthand name (usually native) of the language, which may be used by your custom theme.</li>
	<li><strong>Locale:</strong> the name of the GetText localization file to load for this language.</li>
	<li><strong>ISO Code:</strong> the ISO 639-1 code for the language (two letters).</li>
	<li><strong>Slug:</strong> Used for specifying the language in the URL. Usually the same as the ISO code but can be different if desired.</li>
</ul>

Registered languages can be deactivated, hiding them from being publicly accessible, and excluding them from the All Languages filter in the post listing. The language will still however be available for assigning posts and translations to.

To delete languages, check off the delete box and and then click Save Changes.
